print(round(1.3)) # Devuelve el entero más cercano a 1.3, o sea, 1
print(round(1.5)) # Devuelve el entero más cercano a 1.5. En este caso devuelve aproxima a 2

print(abs(-8)) # Devuelve el valor absoluto de -8, o sea, 8

import math # Importamos esté módulo para usar métodos matemáticos más complejos

print(math.ceil(1.2)) # Redondea al entero superior más cercano. 2
print(math.floor(1.99)) # Redonde al entero inferior más cercano. 1

print(math.isnan(23)) # Devuelve False porque pregunta si 23 no es un número real. En caso contrario devuelve error

print(math.pow(10, 3)) # Devuelve 10 elevado a la 3
print(math.sqrt(9)) # Devuelve la raíz cuadrada de 9














